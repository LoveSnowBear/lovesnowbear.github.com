package sample.skills.data.vo;

import java.util.List;
import java.util.Vector;

public class Branch {
	private String id;
	private String name;

	private List depts = new Vector();

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public List getDepts() {
		return depts;
	}

	public void setDepts(List depts) {
		this.depts = depts;
	}

}
